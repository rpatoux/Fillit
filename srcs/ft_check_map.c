#include "fillit.h"

int		ft_check_point_line(char *str)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	while (str[++i])
	{
		if ((str[i + 1] == '\n' || str[i + 1] == '\0') && i != 4)
			return (1);
		else if (str[i + 1] == '\n' && i == 4)
			i = 0;
		else if (str[i + 1] == '\0' && i == 4)
			return (0);
	}
	return (0);
}
